package geofig.shapes;

public class Line extends Shape {

	public Line() {
		
	}
	
	public Line(String name, String description) {
		super(name, description);
	}
	
	public Line(String[] attributes) {
		super(attributes);
	}
	
	@Override
	public String toString() {
		return super.toString();
	}
	
}
